package com.cengizhan.kose;

public class Main {

    public static void main(String[] args) {
        //for loop
	    for (int i =1 ; i<=10; i++){
	        System.out.println(i);
        }

	    int i = 1;
	    //while loop
	    while (i<=10){
	        System.out.println(i);
	        i++;
        }
	    
		i=1;
		//do while loop
		do{
			System.out.println(i);
			i++;
		}while (i<=10);
	    System.out.println("Döngü bitti");
    }
}
