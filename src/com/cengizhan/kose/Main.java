package com.cengizhan.kose;

public class Main {

    public static void main(String[] args) {
        //for loop
	    for (int i =1 ; i<=10; i++){
	        System.out.println(i);
        }

	    int i = 1;
	    //while loop
	    while (i<=10){
	        System.out.println(i);
	        i++;
        }
	    System.out.println("Döngü bitti");
    }
}
